# ## Was ist Künstliche Intelligenz?
#
# - Wissenschaft und Technik intelligenter Maschinen
# - Systeme, die Aufgaben ausführen, die normalerweise menschliche Intelligenz erfordern
# - Interdisziplinäres Feld: Informatik, Mathematik, Psychologie, Neurowissenschaften, Philosophie
# - Ziel: Maschinen erschaffen, die wahrnehmen, verstehen, lernen und handeln können

# %% [markdown]
#
# ## Vier Ansätze zur KI
#
# |              | **Menschlich**     | **Rational**      |
# |--------------|--------------------|-------------------|
# | **Denken**   | Menschlich denken   | Rational denken  |
# | **Handeln**  | Menschlich handeln  | Rational handeln |
#
# - Verschiedene Definitionen und Ziele
# - Führen zu unterschiedlichen Forschungsrichtungen

