# ## KI-Gebiete
#
# - **Maschinelles Lernen**: Systeme, die aus Erfahrung lernen
# - **Computer Vision**: Bildverstehen und -verarbeitung
# - **Natürliche Sprachverarbeitung**: Textverstehen und -generierung
# - **Robotik**: Physische Agenten in der realen Welt
# - **Wissensrepräsentation**: Modellierung von Wissen

# %% [markdown]
#
# ## KI-Gebiete
#
# - **Planung**: Sequenzen von Aktionen zur Zielerreichung
# - **Expertensysteme**: Spezialisiertes Domänenwissen
# - **Genetische Algorithmen**: Evolutionäre Optimierung
# - **Schlussfolgern unter Unsicherheit**: Umgang mit Vagheit, Fuzzy-Logik
# - **Multi-Agenten-Systeme**: Koordination mehrerer Agenten

# %% [markdown]
#
# ## Stand der Technik: Erfolge
#
# - **Spiele**: Übermenschliche Leistung in Schach, Go, Poker
# - **Spracherkennung**: Nahezu menschliche Genauigkeit
# - **Übersetzung**: Hochwertige automatische Übersetzung
# - **Bilderkennung**: Übertrifft Menschen bei bestimmten Aufgaben
# - **Medizin**: Diagnose von Krankheiten, Medikamentenentdeckung
# - **Autonomes Fahren**: Fortgeschrittene Assistenzsysteme

# %% [markdown]
#
# ## Stand der Technik: Generative KI
#
# - **Large Language Models (LLMs)**
#   - GPT-5, Claude, Gemini
#   - Textgenerierung, Programmierung, Analyse
# - **Bildgenerierung**
#   - DALL-E 3, Midjourney, Stable Diffusion
#   - Fotorealistische und künstlerische Bilder
# - **Multimodale Modelle**
#   - Verarbeitung von Text, Bild, Audio, Video
#   - Vision-Language Models

# %% [markdown]
#
# ## Stand der Technik: Herausforderungen
#
# - **Erklärbarkeit**: Black-Box-Modelle schwer zu interpretieren
# - **Robustheit**: Anfällig für "bösartige" Beispiele
# - **Generalisierung**: Transfer auf neue Domänen schwierig
# - **Ethik & Bias**: Fairness und Diskriminierung
# - **Energieverbrauch**: Training großer Modelle sehr ressourcenintensiv
# - **Halluzinationen**: KI generiert plausible aber falsche Informationen

# %% [markdown]
#
# ## Offene Probleme
#
# - **Allgemeine KI (AGI)**: Noch weit entfernt
# - **Bewusstsein**: Können Maschinen bewusst sein?
# - **Gesunder Menschenverstand**: Schwierig zu formalisieren
# - **Kausalität**: Verstehen von Ursache und Wirkung
# - **Langzeit-Autonomie**: Selbstständiges Lernen und Anpassen
# - **Mensch-KI-Kollaboration**: Optimale Zusammenarbeit

# %% [markdown]
#
# ## Zusammenfassung
#
# - KI: Wissenschaft intelligenter Agenten
# - Vier Ansätze: menschlich/rational × denken/handeln
# - Moderne KI fokussiert auf rationale Agenten
# - Beeindruckende Fortschritte in vielen Bereichen
# - Generative KI revolutioniert viele Anwendungen
# - Wichtige Herausforderungen bleiben bestehen
