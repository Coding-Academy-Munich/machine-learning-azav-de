# ## Was ist LangFlow?
#
# - **Visuelles Tool** für LangChain-Workflows
# - Web-basiertes Interface
# - **Komponenten** per Drag & Drop verbinden
# - Export zu Python-Code
# - **Integration** mit LangChain-Ökosystem

