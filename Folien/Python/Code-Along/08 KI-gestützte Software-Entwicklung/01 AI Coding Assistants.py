# ## Was sind AI Coding Assistants?
#
# - **KI-gestützte Werkzeuge** für Software-Entwicklung
# - Helfen beim Schreiben, Verstehen, Debuggen von Code
# - **Beispiele**:
#   - GitHub Copilot
#   - Claude Code
#   - Cursor
#   - Tabnine
#   - Codeium

# %% [markdown]
#
# ## Was können sie?
#
# - **Code-Vervollständigung**: Vorschläge während Sie tippen
# - **Funktionen generieren**: Aus Kommentar → Code
# - **Tests schreiben**: Automatisch Tests generieren
# - **Bugs finden**: Code-Analyse
# - **Refactoring**: Code verbessern
# - **Dokumentation**: Kommentare und Docs schreiben
# - **Erklärungen**: Code verstehen

# %% [markdown]
#
# ## Werden Programmierer ersetzt?
#
# **Nein!**
#
# - AI ist ein **Werkzeug**, kein Ersatz
# - Wie Taschenrechner → Mathematiker nicht ersetzt
# - Programmierer werden **produktiver**
# - Fokus verschiebt sich zu:
#   - Design und Architektur
#   - Problemlösung
#   - Code-Review
#   - Qualitätssicherung

# %% [markdown]
#
# ## Best Practices
#
# ✅ **Verstehen** Sie den generierten Code
# ✅ **Testen** Sie AI-generierten Code
# ✅ **Überprüfen** Sie auf Sicherheitsprobleme
# ✅ **Lernen** Sie aus AI-Vorschlägen
#
# ❌ **Nicht**: Blind copy-paste
# ❌ **Nicht**: Ohne Tests verwenden
# ❌ **Nicht**: Ohne zu verstehen

